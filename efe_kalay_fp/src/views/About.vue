      <h1>About The Met</h1>
      <p>The Metropolitan Museum of Art of New York City, colloquially "the Met", is the largest art museum in the United States. Its permanent collection contains over 2 million works, divided among 17 curatorial departments. The main building at 1000 Fifth Avenue, along the Museum Mile on the eastern edge of Central Park in Manhattan's Upper East Side, is by area one of the world's largest art galleries. A much smaller second location, The Cloisters at Fort Tryon Park in Upper Manhattan, contains an extensive collection of art, architecture, and artifacts from medieval Europe.

The permanent collection consists of works of art from classical antiquity and ancient Egypt, paintings, and sculptures from nearly all the European masters, and an extensive collection of American and modern art. The Met maintains extensive holdings of African, Asian, Oceanian, Byzantine, and Islamic art. The museum is home to encyclopedic collections of musical instruments, costumes, and accessories, as well as antique weapons and armor from around the world. Several notable interiors, ranging from 1st-century Rome through modern American design, are installed in its galleries.

The Metropolitan Museum of Art was founded in 1870 to bring art and art education to the American people. The Fifth Avenue building opened on February 20, 1872, at 681 Fifth Avenue. In 2020, due to the COVID-19 pandemic it was closed for 202 days, and attracted only 1,124,759 visitors, a drop of 83 percent from 2019, but it still ranked ninth on the list of most-visited art museums in the world.</p>
